    #[test]
    fn load_error_top_level_throw_degrades_gracefully_to_no_location() {
        // Boa 0.21 does not expose `Error.stack` to JS, and module-evaluation
        // promise rejections discard the engine's internal shadow-stack — so a
        // top-level `throw new Error(...)` cannot recover a call-site frame.
        // Per spec §1.1, when no user frame can be identified `location` is
        // None and the reporter falls back to the message-only path. This test
        // pins that contract.
        let f = write_temp_js(
            r#"import { describe } from "zero/test";
function boom() { throw new Error("nope at top"); }
boom();
"#,
        );
        let root = f.path().parent().unwrap();
        let result = run_file(root, f.path());
        let le = result.load_error.expect("expected load_error");
        assert!(
            le.message.contains("nope at top"),
            "message should preserve user-thrown text: {:?}",
            le.message
        );
        // location is allowed to be None here; verifying that no panic / error
        // is reached and the path still surfaces the message is enough.
        let _ = le.location;
    }

    #[test]
    fn location_skips_runtime_frames() {
        // The matcher frames (zero/test internal) are in the stack between the
        // throw site and the user call; the picked location must point at the
        // user file, not at the matcher.
        let f = write_temp_js(
            r#"import { describe, it, expect } from "zero/test";
function callsite() { expect(1).toBe(2); }
describe("g", () => { it("fail", () => callsite()); });
"#,
        );
        let root = f.path().parent().unwrap();
        let result = run_file(root, f.path());
        let failure = result.outcomes[0].failure.as_ref().expect("failure");
        let loc = failure.location.as_ref().expect("location");
        assert_eq!(loc.file, f.path());
        // `callsite` body is on line 2 — assert at least we point at the user file
        // and a plausible line (1..=3 covers any minor numbering variance).
        assert!(
            (1..=3).contains(&loc.line),
            "loc.line should be inside the user file's first few lines, got {} (stack: {:?})",
            loc.line,
            failure.stack
        );
    }

