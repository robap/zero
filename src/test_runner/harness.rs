    #[test]
    fn run_file_with_coverage_returns_coverage_snapshot() {
        use crate::test_runner::coverage::CoverageScope;
        use crate::test_runner::loader::CoverageContext;

        let dir = tempfile::tempdir().unwrap();
        let root = dir.path();
        std::fs::create_dir_all(root.join("src")).unwrap();
        std::fs::write(
            root.join("src").join("foo.ts"),
            "export function bar(){ return 42 }\n",
        )
        .unwrap();
        let test_path = root.join("foo.test.ts");
        std::fs::write(
            &test_path,
            r#"import { describe, it, expect } from "zero/test";
import { bar } from "./src/foo.ts";
describe("g", () => { it("works", () => expect(bar()).toBe(42)); });
"#,
        )
        .unwrap();

        let scope = CoverageScope::new(root.to_path_buf(), root.join("dist"));
        let ctx = Rc::new(CoverageContext::new(scope));
        let outcome = run_file_with_coverage(root, &test_path, Some(ctx.clone()));
        assert!(
            outcome.result.load_error.is_none(),
            "load_error: {:?}",
            outcome.result.load_error
        );
        assert_eq!(outcome.result.outcomes.len(), 1);
        let cov = outcome.coverage.expect("coverage snapshot present");
        let maps = ctx.drain_maps();
        assert!(!maps.is_empty(), "expected at least one instrumented file");
        // The snapshot should include the foo.ts file with bar fn hit.
        let foo_key = maps[0].file.to_string_lossy().to_string();
        let bar_hits = cov
            .get(&foo_key)
            .and_then(|v| v.get("fns"))
            .and_then(|v| v.get("bar"))
            .and_then(|v| v.as_u64())
            .unwrap_or(0);
        assert_eq!(bar_hits, 1, "expected fns.bar=1, snapshot: {cov}");
    }

